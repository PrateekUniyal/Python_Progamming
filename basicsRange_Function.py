# Basics of range function

n = int(input("Please enter the range: "))
for i in range(n):
    print(i)
print("------------------")
for i in range(n+1):
    print(i)
print("------------------")
for i in range(1,n+1):
    print(i)
print("------------------")
for i in range(-5,n+1):
    print(i)
print("------------------")
